                <div class='title-url' style="font-size:35px; line-height:60px; font-family: 'Rufina', serif; text-align:left">
                  General Commodity Taxes: An Evolution
                </div>
                <div class='title-url' style="font-size:1.5rem; line-height:1.4; font-weight:700; font-family: 'Rufina', serif; text-size-adjust:100%; text-align:left ">
                  Is the current tax regime justified?
                </div>
                <br />
                <div class="text-subtitle2" style="font-family:'Rufina', serif">by Neha Nandakumar | September 15th, 2020</div>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <i style="font-size: 14px;font-family: 'Rufina', serif">In this piece, the author explores the trends in commodity taxation in India and critiques the rationale for the transition from good-specific to general taxes. Using specific examples, the author argues that there seems to be no uniform guiding principle or framework determining the goods for which taxes are to be introduced or continued.
                </i>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Since the introduction of the GST, there has been a growing debate on the fairness of the tax rates, and exemptions to it. These questions, however, are based on the assumption that most goods ought to be taxed. In this article, I examine this assumption by analysing trends in commodity taxation in Indian history. For this, I turn to the transition from good-specific taxes to general commodity taxes. At independence, only 12 goods, barring miscellaneous commodities, were subjected to a sales tax by the Centre. However, by the 1975-1976 budget, a general rate of minimum 1% was charged on all goods, unless specifically exempted. How did this come to be? What can this transition tell us about the rationale guiding commodity taxation?
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The goods subjected to union excise duties at independence were sugar, matches, steel ingots, tyres, tobacco, vegetable products, betel-nuts, motor spirit, kerosene, coffee, tea and coal cess.<sup>1</sup> From this point onwards, more and more goods have been subjected to taxation for a variety of reasons. I will illustrate this process by using some key examples of taxed commodities.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    In the budget of 1949-1950, taxes were imposed on fine, coarse and medium mill-made cloth <sup>2</sup> to replace the losses caused by the abolition of the salt tax and provide a boost to the handloom industry. In 1954, because cotton was taxed in lieu of salt, artificial silk fabrics were also taxed to ensure fair economic competition. <sup>3</sup> Also as cotton taxation had been legitimized, taxing other commodities with similar features could also be concluded to be legitimate.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    In 1954, taxes were also imposed on indigenous industries that had developed under protectionist policies to offset the risks of relying on customs duties. Then, in the budgets of 1960-61, more industrial material was subjected to taxation, but due to the substantial rise in industrial production. <sup>4</sup> Between 1970 and 1974, nearly 60 new goods were added to the list, a reason for which was to reduce wealth inequality, to be achieved by taxing goods that were chiefly consumed by those of “high living”, like lipsticks, playing cards and ready-made garments. <sup>5</sup> In addition to these examples, many goods were brought under the tax net without specific reasons being quoted, in which case only the expected revenue gains were mentioned.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Finally, in the budget of 1975-76, <sup>6</sup> a rate of 1 percent ad valorem was imposed on all goods not otherwise specified, apart from certain exemptions. The reason for this was to “widen the coverage of taxable goods” and “provide a more dependable information base for future revenue raising exercises”. In these near-thirty years, a large number of goods had been taxed for a variety of reasons, but without a guiding framework or principle of what counts as a ‘good enough’ reason. It appears that taxation was decided upon based on the circumstances, and once the Central taxation of commodities commenced, more reasons to continue it were to be subsequently found. Every commodity held the potential for taxation, but what had turned out to be the case is that some reason to actually implement it had to be found. In 1975, that was no longer necessary. Why?
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    An explanation for this can be found by examining what happens to a commodity once it has been subjected to taxation. From the examples mentioned, one can see that once a commodity begins to be taxed, the original reason for taxing it ceases to matter, and it is seen as an independent entity that is taxed. The starkest instance of this was seen in the cotton-silk example. This understanding of the impact of taxation also explains why a commodity continues to be taxed even after its original motivation ceases to hold. For example, if goods like lipsticks, shampoos and pressure cookers that relied upon their usage only by wealthier sections of society became more widespread, the logic of this tax would not stand. While the implementation rested on a condition, it would not be reexamined if the condition itself ceased to be valid. Consider this as well — many goods were introduced solely because of their extensive usage, such as artificial silk. This taxation exists simultaneously with the goods being taxed because of their exclusive usage. Yet, this is not considered as a problem, because the goods are viewed as independent entities.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    What grants commodities and forms of taxation this legitimacy which allows them to stay, independent of the rationale behind their introduction? One reason could be that being a sales tax, it does not require people to modify their daily activities to pay the tax. This allows the tax to become part of everyday life without bringing itself to the notice of the taxpayer. By becoming part of their lives, it begins to be seen as normal, reducing opposition to it even though the reasons for opposition might still be justified. Further, the goods generally begin to be taxed at relatively low rates. For example, when the general duty was introduced in the Union Budget of 1975-76, the tax rate was only 1 percent. By 1986, however, the rate was increased to 12%. <sup>7</sup> The normalcy of the tax could thus also be derived from its inconspicuousness, both in terms of the point of imposition — the goods — as well as the manner of its imposition, given the mostly gradual rise in rates.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    While people weaken their stance on this tax even now, the state’s interest in it is unwavering, if not increasing. The sales tax acts as an effective source of revenue, and now that people are not actively opposed to it anymore, the state too has no incentive to give up an efficient source of revenue. The state adjusts its expenditure in accordance with the revenue it receives. Therefore, a fall in the revenue can constrain expenditure goals which have been made keeping in mind the increase in revenue, making the government unwilling to remove taxes from goods. The sales tax becomes normal.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    This may not be the only reason for the transition from good-specific taxes to general taxes. However, this conclusion presents the opportunity to build deeper critiques of the goods currently taxed. Questions must go beyond why something is not exempt, to why it is included at all. Taxation of all commodities is neither an inevitability nor the only way things can be. It is the outcome of taxation policies built on a history of other taxation policies. Therefore, our understanding of present forms of taxation need not be restricted to assuming their existence but can begin from questioning what purpose they serve today, and whether that is a good enough reason for us.
                </p>
