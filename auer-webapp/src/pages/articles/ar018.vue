                <div class='title-url' style="font-size:35px; line-height:60px; font-family: 'Rufina', serif; text-align:left">
                  Not the New Oil
                </div>
                <div class='title-url' style="font-size:1.5rem; line-height:1.4; font-weight:700; font-family: 'Rufina', serif; text-size-adjust:100%; text-align:left ">
                  Exploring the Role of Information as a Business Asset
                </div>
                <br/>
                <div class="text-subtitle2" style="font-family:'Rufina', serif">by Advaita Singh | September 8th, 2020</div>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <i style="font-size: 14px;font-family: 'Rufina', serif"> In this piece, the author explains how the use of data by an organization can be important in its future revenue generation. Consequently, the author argues that data should be viewed as a ‘business asset’, by reviewing key inferences from the book ‘Infonomics: How to Monetize, Manage, and Measure Information as an Asset for Competitive Advantage’ by ‘ Douglas B. Laney.
                </i>
            </div>
            <div style="padding: 10px 0px 10px 0px; text-align: center">
                <p style="font-size: 18px; font-family: 'Rufina', serif;">
                    <b><i>“Without big data, you are blind and deaf and in the middle of a freeway.”</i></b>
                </p>
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    <b><i> – Geoffrey Moore</i></b>
                </p>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    As dilettantes and students of economics, if anyone can appreciate the immense value of information in decision making, it is us. Ranging from used car markets to insurance companies, spanning across consumers in a marketplace to producers in their offices making production decisions, having complete and perfect information is a prerequisite for astute decision making.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Since the dawn of the information age, the internet has made data highly accessible and cheap. It has opened another channel through which businesses can interact with consumers, and hence, has increased the amount of information that can be mined. Automated analytics and AI have revolutionized the kind of data available as well. With data mining and accumulation of Big Data, businesses have access to more than demographics and consumer preferences, but also to their personality types, consumer psychology and lifestyle patterns.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Galvanised by the profit motive, in this information age, businesses are trying to exploit the massive resource of information available to them. Without their mailing sheets, consumer preference data, cost-charts and intellectual property, a business in this decade would be as lost as a deer in headlights. By using just one information set, for instance, a consumer dataset, a business has the necessary insights on consumer preferences for product development, marketing campaigns, pricing decisions etc. With information being such a crucial piece of the business puzzle, it is bewildering as to how neither the law nor the accounting boards recognise it as a formal economic asset; which is why, in case any unforeseen calamity that falls upon a firm, insurance companies will not cover the cost of the information lost. One of the primary reasons for these policies to linger is that information as an asset is nearly impossible to place a value on. This has impelled economists to explore the complexities of information’s nature as an asset — which will enable and empower businesses to manage, measure and monetise it.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Douglas B. Laney noticed the unexplored potential of information and wrote ‘Infonomics: How to Monetize, Manage, and Measure Information as an Asset for Competitive Advantage’ <sup>1</sup> to explore the idea further. This article aims at highlighting some of the key ideas explored in the book and to comment upon the need for realising the inseparable relationship between businesses and information.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    <b>The Economic Characteristics of Information as an Asset</b>
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    An asset is “A resource controlled by the entity as a result of past events and from which future economic benefits are expected to flow to the entity” <sup>2</sup>. Does information fit this definition? Well, the information generated from past events (such as consumer details from the sale of goods and services) helps businesses make decisions about marketing, pricing, inventory management, product design and more which will lead to future economic benefits. Given enough protection and careful use, information can be controlled to a great extent. Hence, there is enough basis for information to be considered a business asset.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Some properties of information are obvious and evident; information is non-depletable, easily replicable, non-rival and highly reusable. What is perhaps not as explicit is that information is a regenerative asset — the use of information and its application leads to more information creation. Moreover, it has a high potential ancillary value — its potential utility for the business organisation is much beyond the process that initially consumes/creates it. This stands beside the additional potential generated from the network effects. It is a resource worth investing in, especially due to the low storage and transportation costs thanks to the advancements in technology. It is cheaper now than ever before to hold tons of data safely, including the bandwidth costs of sending the data.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    However, many businesses hesitate to take the leap in investing in information collection and analysis. Being a costly endeavour, managers and CEOs find it difficult to explain the long-term benefits of their massive information-related investment to their shareholders. Moreover, there is a high opportunity cost associated with investing in information collection and analysis (for example, the money invested could be used for expansion purposes) which implies that unless additional capital is raised, the money will have to be diverted from other areas to fuel the project. Other issues discourage businesses from investing (such as late return on investments, not being able to maximise the information asset maturity etc.), however, one of the strongest reasons is underestimating the potential and power of information.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    As much as information belongs on the balance sheet, it cannot be treated like any other asset such as oil (despite what the popular saying ‘data is the new oil’ propagates). Due to the unique nature of information, standard economic principles just cannot be applied to it which becomes a hindrance when trying to monetise, process and trade data. This raises an interesting discussion on the utility, potential and opportunity cost of information.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    <b>The Economics in Infonomics</b>
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The utility of information is a distinct conversation for the simple reason that information is increasingly being consumed by robots, technology or machines which fundamentally do not experience satisfaction as humans do. This requires us to tailor our existing theories to suit the needs of information’s unique properties. Human consumption of information initially yields high positive marginal utility and subsequent consumption only yields positive marginal utility if the information set is novel. If the user is exposed to the identical information sets, then marginal utility diminishes rapidly and could even become negative. In contrast, when machines are fed identical sets, the marginal utility is simply zero. However, information processing has attached costs, therefore, one can consider the marginal utility to be negative for identical sets processed by machines.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The marginal utility of information consumption and processing is essential in designing for data architects. Since similar information gives zero utility and only piles on the processing costs, data architects are careful about what they input. To avoid undesirable results caused by identical information, data architects only transmit distinct, differential or derivative data.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    <b>The Information Market</b>
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    To truly appreciate the potential of information-application in a business, one has to grapple with the enormous array of information at the firm’s disposal and understand how those sets interact with each other. One reason why the potential of information is seriously underestimated by stakeholders is that there is a lack of clarity on the kinds of information available.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    In conducting its daily activities, a firm generates information — data about consumer preferences (mailing lists, busy hours and seasons, products in trend, most effective marketing colors and more), production tactics (cheapest raw material sources, employee productivity etc.), logistics (such as quickest delivery routes). This data has to be actively sought to optimise decisions, however, since the pie is in the house, it is relatively cheap and easy to access. Apart from this internal information, a business has a host of sources to tap into. Marrying external data (about current affairs, trending topics, and ‘what’s in’) with what the business already knows (thanks to internal data) can create the perfect concoction to maximise profits. For example, if a popular TV show is coming to an end, then retailers can use this external information to keep official merchandise in stock.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Increasingly, information bartering is becoming a popular way of negotiating better business contracts. Essentially, one party offers the other access to certain information sets in return for favourable contract terms. An alternate way to monetise data is to sell it like a good.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    As more and more firms are looking at selling their data, it has become increasingly clear that information demands a market of its own. However, it’s non-depletable nature and other characteristics pose a hurdle in using classic demand-supply models. Given that information can be used simultaneously, and does not deplete during consumption, its supply appears to be infinite. Therefore, the equilibrium price is achieved using cost-plus pricing techniques. Suppliers exert control over the market by moderating the number of buyers. Since once information is sold and is ‘out there’, a business has forever lost control of it. The ease of replicability poses the risk of free copies being available instantly after the first sale, which causes market saturation and exerts downward pressure on the price. Hence, to maximise profit, a seller will try to sell to as many buyers as possible simultaneously to avoid replication.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    In a competitive market, product differentiation comes into play with suppliers competing on better quality. Suppliers add value to their information by making it more comprehensive, more credible and maybe adding features like analytics to it. Improving the quality of their information set allows sellers to charge a premium — this is evident in the textbook market when producers try to enrich their product using interactive online services, extra questions etc. In a way, free or cheaper sources of information compel sellers to step up their game.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    <b>Conclusion</b>
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    These are exciting times. Without a doubt, we are experiencing a pivoting era of business operation. Never before has there been so much easy access to information about the world and the billions of consumers therein. That is, until tomorrow and the day after that… Exploring the nature of information as an asset is the first crucial step in extracting most of what this resource has to offer.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    It may take ages for the law and accounting boards to recognise that the immense value carried in a pen drive extends far beyond the value of the drive itself. But until then, let’s don our detective hats and grab the magnifying glass because the data is out there it just needs to be uncovered.
                </p>
