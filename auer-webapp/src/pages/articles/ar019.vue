                <div class='title-url' style="font-size:35px; line-height:60px; font-family: 'Rufina', serif; text-align:left">
                  Competition Control in Digital Markets
                </div>
                <div class='title-url' style="font-size:1.5rem; line-height:1.4; font-weight:700; font-family: 'Rufina', serif; text-size-adjust:100%; text-align:left ">
                  How are digital firms misbehaving?
                </div>
                <br />
                <div class="text-subtitle2" style="font-family:'Rufina', serif">by AUER Staff | September 4th, 2020</div>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <i style="font-size: 14px;font-family: 'Rufina', serif">The exponential growth of digital markets and data-driven technology in recent years has introduced novel products and services to the 21st century consumer. In this article, the author argues that such rapid expansion has come at the cost of anti-competitive behaviour, analysing how digital firms misuse their power.
                </i>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    After the advent of the Internet, digitalization and data-driven technologies have cascaded through various industries and applications. The 21st century consumer is exposed to a myriad of innovative products and services, increasingly provided online, accessible at any place or time and mostly charged at zero or negligible prices. This ecosystem of innovation where technology drives trade of data, services and goods, is coined as the ‘digital economy’ – one of the world’s leading market spaces.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    While the expansion of the digital economy has transcended into substantial economic growth and is often proclaimed as the fourth industrial revolution, such technological shift has raised serious concerns among competition authorities globally. The suspicion is that digital giants, namely Alphabet, Amazon and Facebook among others, have created concentrated and impenetrable markets that receive little or no scrutiny from antitrust doctrines. <sup>1</sup> Contrastingly however, many fear that enforcement efforts entail a high risk of chilling innovation, arguing that the risk of false positives prohibit growth. The basis for this argument first arose from Richard Posner’s influential essay in 2000, published during the high-profile litigation of Microsoft vs the United States of America (1998), where he opines that digital markets are not novel enough to warrant explicitly different or reformative antitrust rules and advocates a pro-defendant position. <sup>2</sup> However, with the current unprecedented growth of the digital economy, Posner’s view has garnered criticism. <sup>3</sup> Therefore, at the foot of this trade-off between innovation and competition, this article attempts to understand how digital markets pose threats to competition.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                   A direct consequence of the characteristics exhibited by digital platforms, including network effects, extreme returns to scale and collection of large units of data, is the creation of economies of scope. This implies that once a digital platform is efficient at providing one service, it is easier for it to offer complementary services. In other words, the production of one good reduces the cost of producing another related good.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Together, characteristics like the economies of scale and scope, imply that from a static efficiency perspective, concentration in market is desirable and often, inevitable. This is because it is wasteful to build several platforms serving the same role since costs are decreasing in size and the bigger the market, the better for the consumer. However, from a competition policy perspective, there is reasonable concern that such durable concentration of power lays groundwork for engagement in anti-competitive behaviour.
                </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
                   Discouraging Multihoming
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Often, users of digital platforms simultaneously use multiple and differentiated platforms to access good and services. Since services are mostly free, users instead pay attention costs to these platforms. For example, users of social media services might spend time on varied platforms that differ in features provided. This use of multiple platforms is referred to as multihoming, and is often considered to have a pro-competitive effect. <sup>4</sup> This is because, if a platform operates a network, say a popular social networking site, and attempts to raise the attention costs for a user above a competitive level through techniques such as displaying too many advertisements, the user can switch to a rival network. This disciplines the platform operator’s ability to raise user costs to the extent of causing consumer harm.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                   Continuing the example, however, suppose that the platform operators decides to acquire the rival. In the case that the network markets have differentiated characteristics, competition authorities using traditional market definitions can conclude that the firms operate in different markets. Subsequently, an analyst would likely approve the proposed acquisition. This is what was observed in Facebook’s clearance to acquire Instagram, WhatsApp, tbh and dozen more small product companies that offered complimentary yet differentiated services than Facebook’s network – all with little or no conditions from competition authorities. <sup>5</sup> Control of multiple differentiated platforms prevent users that were multihoming and restrict them to operate under one single entity, where the merged firm can exercise its power in many anti-competitive ways.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    For example, the merged firm can exercise its power to extract users’ attention. To do so, the firm can employ a cross-platform targeted advertising strategy using data from all its platforms, leaving the user with no choice but to spend additional time on these platforms or leave all the platforms altogether. While the horizontal merger guidelines in jurisdictions such as the United States, use diversion ratios – ratio of users that would potentially switch to the rival network that the firm proposes to merge with – to understand the impacts of the merger and prevent creation of a monopoly, anticompetitive harm is still unnoticed due to focus on “price effects” to understand impacts in differentiated markets. The formula used to calculate diversion ratios relies on quantification particularly focused on price comparisons. However, since digital markets are often zero-priced, traditional antitrust analysis finds it hard to “prove” harm, allowing prevention of multihoming, and thereby leaving consumers no choice but to be prey to a machine learned advertising strategy. To illustrate, suppose that an outdoor watch company chooses to use Facebook as an advertising platform. To maximize effective viewership, Facebook records all users that travel outdoors through search clicks or use similar geotagging on Instagram as well as Facebook, and displays repeated advertisement on Facebook, Instagram as well as Audience Network. This is indeed what Facebook did with its advertising client, Garmin. <sup>6</sup>
                </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
                   Algorithmic Collusion
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Acts of collusion include price fixing, synchronized advertising, and information trading, that help firms sustain profits over and beyond non-cooperative levels. Consequently, this results in a deadweight loss in the society.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                   In the context of digital markets, interestingly, the possibility of anticompetitive collusions rises through the use of algorithms. Referred to as algorithmic collusion, in these agreements, competitive digital firms can influence prices without necessarily entering in an agreement – through simple modification of code. <sup>7</sup> Moreover, since most algorithms automatically assign and monitor prices, there is often no human interaction to engage in this process. Other possible methods of such collusion could include outsourcing the creation of algorithms for competitive firms to the same IT companies and programmers, where the company develops a pricing algorithm that allows collusion. Post such agreements, pricing algorithms are set to execute trigger strategies. If the prices on the two platforms are at par, the algorithm runs smoothly, however, if one firm deviates from the agreement, the algorithm automatically breaks. Consequently, there is little incentive to deviate once such an agreement is drawn. Moreover, unlike traditional markets, where authorities can observe price wars, digital market collusions can only often be observed though algorithmic errors.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    While algorithms present many pro-competitive efficiencies associated with automation, cost reduction and better allocation of resources, they are evidently also capable of distorting digital markets by creating incentives to collude – it is exceptionally hard for competition authorities to recognize automated collusion processes. While there is a dearth of empirical evidence on the effects of algorithmic collusion, there are examples to substantiate the danger. For instance, in 2015, an Amazon seller, David Topkins was charged by the Department of Justice for coordinating with other sellers to set the prices of posters. <sup>8</sup> Other forms of technology-based collusion, such as blockchain collusion are also gaining significant importance in academic literature.
                </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
                   Splitting Rents
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Often, digital platforms like Google, not only bring advertisers and users together but also buyers and sellers of offline products. For example, a user might search for “gymnasiums” and be shown appropriate advertisements. This user therefore, by virtue of the digital platform, is also likely to engage in an offline transaction – the membership of a gymnasium. In the case that a gymnasium is listed as an advertising counterparty to Google, and would pay the search provider additional rents to access users’ attention with a favoured spot or repeated targeted advertising strategy, it could steer local markets in its favour and increase local concentration in the gymnasium market. While a search provider can simply provide neutral search results, it has strong incentive to seek rents for biased support, the ultimate effect of which is closure of non-favoured yet equally competent sellers. While the scope for harm in offline markets with strong competitive forces is low since market consolidation due to steering is unlikely, in markets already enshrined with barriers to entry, this strategy can further cause concentration, thereby drawing suspicion from antitrust authorities.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                   While antitrust authorities have certainty redirected attention to such harm, charging giants such as Google enormous fees for steering users towards its own comparison-shopping services, there is still a long way to go in order to fully understand the complexity and the indirect effects of online services. For instance, Amazon is also accused of favouring less efficient products on its platforms through rent seeking. The trade-off that competition authorities must consider is consumer’s choice of product versus profitable engagements for the digital platform.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Digital firms with strong dominant positions are constantly furthering their market strength not necessarily on merits but rather exclusion and self-preference – compelling further research and intervention from competition authorities.
                </p>
