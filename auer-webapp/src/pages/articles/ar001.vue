                <div class='title-url' style="font-size:35px; line-height:60px; font-family: 'Rufina', serif; text-align:left">
                  Universal Basic Income: A Critical Analysis of the Theory and Evidence
                </div>
                <div class='title-url' style="font-size:1.5rem; line-height:1.4; font-weight:700; font-family: 'Rufina', serif; text-size-adjust:100%; text-align:left ">
                  A strong case for implementation?
                </div>
                <br />
                <div class="text-subtitle2" style="font-family:'Rufina', serif">by Akash Chowdhury | September 8th, 2020</div>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <i style="font-size: 14px;font-family: 'Rufina', serif">In this piece, the author explores the historic evolution of Universal Basic Income and its growing present-day relevance, exploring some key challenges that persist in its implementation.
                </i>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    Radical times call for radical measures.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The idea of a universal basic income (UBI) sounds shocking – scandalous even – to many. And rightly so, given that there’s been no such program in operation anywhere around the world for the last many decades. There has, however, been a recent surge in interest around this subject. During the 2019 Lok Sabha election campaign, both the major Indian parties – the BJP and the Congress – promised to offer some kind of (conditional) cash transfer. In the run-up to the US Presidential election of 2020, Democratic Party candidate Andrew Yang promised a UBI to the tune of $1000 for each American (every month).
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    In order to be able to have an objective assessment of the idea of the UBI, it is important to do three things. First, we must look back on the past decades to try and make sense of the success (or otherwise) of UBI-like programs undertaken around the world. Second, we should make an attempt to analyze the idea in terms of its ideational import. Finally, as is the primary requirement of any process of analysis, we have to be able to draw on the literature that is available on this subject – the experimental designs, conclusions, comparative studies, behavioral inferences, <i>et cetera</i> – to make a conclusive argument regarding the efficacy of the program in question.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    This article is an attempt at summarizing the available evidence on this topic, and drawing some preliminary conclusions based on it. Of course, there is a lot of research work being conducted in this area which would only serve to modify the inferences drawn here – all of which must be considered a part of the evidence available for us to learn from before deciding on a program like the UBI.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Even though UBI might sound like a radically new idea, it is not. It was first used in an 18<sup>th</sup> - century English village – Speenhamland – to relieve the parish from having to look after a growing number of people forced into poverty because of a rise in the price of food-grains.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Within the next few decades, the program spread across all England, but was soon discontinued owing to growing opposition from the economists and policy-makers of the time <sup>1</sup> . They were concerned by the population growth since the start of its use and held it (the UBI) responsible for more children (Malthus), lower wages (Marx) and an overall prosperity - drain (Ricardo). Despite their criticism, it is interesting to note that present-day researchers (such as Rutger Bregman and Walter Trattner) call it a case of misattribution, saying it was the medical discoveries of the time and the invention of the mechanical thresher that led to population growth and fall in wages, respectively.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    On the ideational plane, the UBI is an argument in favor of cognitive capitalism. In stark contrast to a Fordist accumulation regime, the concept of cognitive capitalism seeks to incorporate the growth of the human intellect into the general human productivity analysis. Under the neo-classical framework, however, labor is quantified merely by the number of hours of work put in for the sake of production. How is the intellectual effort that is made by the employee measured? It is not, and this is where, the theory of cognitive capitalism argues that change is needed for the very simple reason that there has been unprecedented intellectual growth of the population over the years that is mostly fueled by education. A five-hour shift by a person today accomplishes close to twice the amount of work a twelve-hour shift by somebody in the 1950s or 60s did. <i>Total factor productivity</i> is the name afforded to the growth in output that can <i>not</i> be attributed to change in labor or capital stock (‘it is what is left after we have accounted for everything we can measure’<sup>3</sup>). There is, thus, leftover output that nobody knows who produced. It is the capitalists (the capital-owners, the CEOs, and others) who earn increasingly more because of TFP growth. This, however, does not trickle down to the employees in the form of additional compensation since ‘they didn’t produce it’. This is the reason even though net productivity has risen by 72.2% between 1973 and 2014 in the United States, real average hourly compensation has risen by only 42.5% (the median compensation has grown by only 8.7%, indicating the increasingly lower bargaining power of those earning around the minimum-wage level)<sup>4</sup>. This breakdown in the wage-productivity nexus<sup>5</sup> is what the UBI strikes at, ensuring workers receive at least as much as they need in order to survive. Crucially, this may still fall short of the gross productivity compensation required, but the idea of redistribution (toward the ones who barely make subsistence-living) is an ideal one to use to lay the ground for greater reform in the future.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    It is important to note that recent evidence shows that on a micro-level, UBI is not nearly as evil as it’s intuitively made out to be. A potential proxy for a UBI is tax, and there is sufficient evidence to show real work effort does not correspond to top tax rates <sup>6</sup>. It can thus be said that <i>at least</i> the rich will not begin to work less because of the UBI. A change in the way income-tax was collected in Switzerland meant that citizens did not have to pay any tax for the years 1997 and 1998. Despite this, the labor supply did not change <i>at all</i> during this time <sup>7</sup>, suggesting ordinary people are not incentivized enough by the prospect of some free money to work less. This is corroborated by an RCT experiment in Ghana wherein poor village women were given the opportunity to sell hand-woven bags, while only a few within that group got a goat and some training to make the best use of it. Findings show those with the goat made more bags and earned significantly more than those without, suggesting a positive work-effect of receiving an asset that provides enough earning to take care of subsistence-level needs<sup>8</sup>. There is thus no evidence that would suggest cash transfers make the poor lazy<sup>9</sup>, but there is some to suggest that a low-value transfer may not yield long-term income benefits<sup>10</sup>.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    It is important to reiterate here that our objective – through UBI – is not long-term income-growth, but to address the problem of many people being unable to make their ends meet, which can have a decisively negative effect on their engaged productivity.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    UBI is an unconditional <i>cash</i> transfer. It thus helps free the process of all the ills that plague an in-kind program, most notably the leakage of transfer-commodity. For instance, according to the Economic Survey of 2016-17, only 30% of the sanctioned PDS kerosene could reach the poor in India. Research by Prabhat Barnwal of Columbia University shows that transferring the LPG subsidy directly to the bank accounts of the beneficiaries helped save the Indian government $2 billion each year –25% of the expenditure incurred on the program. The fact that it is <i>unconditional</i> also frees it from the problems that conditional transfer programs face, such as targeting. For example, it is difficult and costly – but incredibly important – to account for the dynamic nature of poverty (25% of the poor moved out of poverty between 2004-05 and 2011-12)<sup>11</sup>. And thus, there is a case to be made for an unconditional cash benefit, but it is important to bear the context in mind.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The world is faces a major global pandemic that – at the time of writing this article – has afflicted more than 26 million lives. Consequently, more than 850,000 people have succumbed to death already, with India officially declared as the third worst-affected country in the world. Against this background, there are some serious issues to bear in mind when considering the possibility of a pan-India UBI.  During lockdowns, goods and services are not produced anymore, leading to major supply-side disruptions. A transfer of a sum of money does not ensure that the individual(s) will be able to make use of it. There might also be localized spikes in the price level, corroding the value of the cash transfer made. Finally, there is a greater possibility of diverting cash (for corrupt uses) than transferred commodities. Besides, a UBI most certainly requires access to banking facilities. Nearly a third of all Indian adults still do not have access to bank accounts or banks<sup>12</sup>. All of this must be part of the debate around introducing a UBI in India.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    There is, hence, substantial criticism to take into account and address before implementing an Indian UBI. It is, however, crucial to bear in mind how life-saving Rs 1000 can prove for a poor individual who has three children to feed but no income - thanks to the pandemic. Every policy-debate, discussion, and decision must proceed with the needs of such people in mind.
                </p>
