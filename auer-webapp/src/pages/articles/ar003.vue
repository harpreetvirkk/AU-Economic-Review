                <div class='title-url' style="font-size:35px; line-height:60px; font-family: 'Rufina', serif; text-align:left">
                  How has the E-Commerce Industry Checked Out in the Pandemic Season?
                </div>
                <div class='title-url' style="font-size:1.5rem; line-height:1.4; font-weight:700; font-family: 'Rufina', serif; text-size-adjust:100%; text-align:left ">
                  A Closer Look at Growth Potential and Challenges
                </div>
                <br />
                <div class="text-subtitle2" style="font-family:'Rufina', serif">by Mansi Ramani | September 8th, 2020</div>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <i style="font-size: 14px;font-family: 'Rufina', serif">In this piece, the author explores how the E-Commerce space has evolved with the growing challenges of the pandemic, analyzing the trajectory of firms such as Amazon and Flipkart, and detailing the growth potential as well as challenges that lie in the journey ahead.
                </i>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    ‘Trying something new’ had been the trend throughout the lockdown run – from exercising at home to the pandemic baking spree. Online grocery shopping was ‘something new’ my mom tried for the first time – though she thought she could never trust it. And, she was not the only one. Globally, 1 out of 5 recent online grocery shoppers were first-time customers. And so were more than 7 lakh shoppers at Myntra’s ‘End of Reason’ sale this year<sup>1</sup>. Indian E-commerce has rapidly gained traction in the past few years. Yet, it is with the pandemic that it has seen a boom like never before.
                </p>
            </div>
            <div class="col-md-8" style="padding: 10px 40px 10px 40px">
                <q-img
                src="https://img.techpowerup.org/200908/ar003-fig1.png"
                style="width: 100%, height: auto"
                >
                </q-img>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    While India’s lockdown(s) halted most business operations, the sale of ‘essential items’ was still permitted. Even without marketing, there was plenty demand since panic had influenced people to stock up. There was growing hesitation towards going to groceries and supermarkets as they attracted larger, lockdown-induced crowds. Besides, work from home was keeping most people busy. This gave rise to an opportunity that not only E-grocers like BigBasket and Grofers, but also horizontal marketplaces like Flipkart and Amazon were determined to grab with both hands.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The current scale of operations, however, posed a serious issue. The e-tailers had limited stocks and buffer arrangements<sup>2</sup> against the large-scale upsurge in demand. Consequently, ‘Out of Stock’ messages on their shopping websites were becoming very common. Simultaneously, replenishments were slow. State border shutdowns held some supplies back. Even if the supplies got through, in some regions, local authorities were ordering warehouses shut. Amazon reported that its operations were restricted to a ‘minuscule’ number of warehouses.<sup>3</sup>  Grofers too, mentioned repeated shutdowns<sup>4</sup>. To add to this were other logistical issues, like reduced manpower at warehouses <sup>5</sup> and getting licenses to store groceries in non-grocery warehouses<sup>6</sup>.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    These supply disruptions had the e-tailers turn to hyperlocal delivery models – picking up goods from neighborhood stores and supermarkets to fulfill orders. Though, until now Flipkart had mostly used its <i>kirana store</i> partners for last-mile deliveries <sup>7</sup>, under its new 90-minute delivery service ‘Flipkart Quick’, it is now planning to lay down a ‘whole network of <i>kirana stores</i>’ for hyperlocal delivery and is already collaborating with Spencer’s Retail. The latter would sell on Flipkart, whose delivery fleet would then source and deliver the orders from Spencer’s offline stores. Flipkart has also teamed up with Ninjacart to source fresh fruits and vegetables from farmers and is in talks with Hindustan Unilever, ITC and DMart.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Interestingly, though Amazon had not shown interest for the hyperlocal model <sup>8</sup> earlier, it is also now piloting with 5000 corner stores selling on its website. Additionally, it plans to invest Rs. 10 crores in its ‘Local shops on Amazon’ initiative<sup>9</sup>. Thinking out of its box, it is also joining force. with the Indian Railways and using the COVID-19 Special Parcel Trains for transporting consignments along 55 lanes across the country<sup>10</sup>. Evidently, an adaptive shift in e-commerce supply chains is imminent.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The other end of the supply chain – doorstep deliveries – struggled as well. As workers left for home, e-commerce was left with an acute shortage of delivery personnel. Among last-minute recruits and those who had stayed back, the morale was low, as local authorities stopped and sometimes beat and arrested them on route while the e-tailers struggled to get passes for them, separately in each state. The result was a huge rise in cancellations, lost revenue, and suspended operations.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    While attempting to work with the current authorities and instructions to get the operations back on track, the e-tailers were also compelled to consider other delivery options. In an innovative solution, Flipkart and BigBasket tied-up with Uber <sup>11</sup> for their last mile deliveries and Grofers, with Zomato<sup>12</sup>.  While this compensated for the lack of delivery persons, it had its drawbacks. For example, with its own van or four-wheeler, while Flipkart managed to fulfil approximately 32 orders per shipment, with Uber, it could only fit in 5-6 orders. This, combined with more than twice the amount of orders being placed meant that the cost inefficiencies ran high.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    While such partnership costs are temporary, the pandemic instilled some long-term costs into the E-commerce business model as well. Running operations amidst the pandemic required that the e-tailers provide PPE kits, sanitizers and masks for their workers. Even larger are the costs of insurance covers for delivery staff which range from Rs 50,000 to Rs. 5 lakhs per employee. At this end, while Flipkart and BigBasket bought insurance plans for their employees, Amazon launched a Relief Fund of $25 million for the members of Amazon’s Delivery Service Partner Program. Meanwhile, Grofers incentivized its workers by awarding them with essential goods at the end of the working day<sup>13</sup>.  Needless to say, for as long as uncertainty about Covid-19 looms and even for months after, these costs are undoubtedly here to stay.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Probing deeper into costs, brings me to the costs of returns as well. Though these might rarely apply to groceries, they have become a painful matter for firms like Flipkart and Amazon, especially for apparels. With the onset of the pandemic, returned items cannot go back on for sale without going through a long list of safety protocols including 48-hour isolation, steam ironing, sanitization and other quality measures such as sterilization of packages in UV chambers. In fact, Flipkart-owned Myntra had to double the investments for the sanitization process<sup>14</sup>.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    These inspections would have still been welcome if orders for apparels flowed in. Groceries are known for having low margins – barely 2-4% – and so, horizontal fronts majorly rely on fashion and electronics sales for their revenue. However, the fashion market is sinking as more people work from home. Costlier workwear and non-essentials are rarely on anybody’s shopping list. The white goods market has not fared any better in sales of goods like ACs and refrigerators.  Typically, sales of such goods go up with summer temperatures, however, this year has only witnessed downfalls owing to the weakened consumer sentiment that the pandemic has entailed<sup>15</sup>.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    For most industries – tourism, aviation, automobiles and MSMEs <sup>16</sup>, the effects of Covid-19 are unprecedented and rather disastrous. For others, such as the EdTech and online streaming space, growth has in fact thrived. However, while most businesses were faced with the question of either survival or growth, E-commerce firms were faced with both, challenges and opportunities equally large. This article, through understanding how top players in the online marketspace have responded to the challenge, argues that the space has displayed transformational potential with its innovative adaptability in the pandemic season. And yet, there is even more waiting in the cart – the impact of JioMart’s entry into groceries, introduction of Flipkart’s ‘Flipkart Quick’ that promises to bring you anything from groceries to electronics within 90-minutes and Amazon and Flipkart’s venture into E-Pharmacy<sup>17</sup>. For now though, it’s time to check out.
                </p>
