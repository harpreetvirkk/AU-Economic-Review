                <div class='title-url' style="font-size:35px; line-height:60px; font-family: 'Rufina', serif; text-align:left">
                  Is culture really the sole determinant of inequality across countries?
                </div>
                <div class='title-url' style="font-size:1.5rem; line-height:1.4; font-weight:700; font-family: 'Rufina', serif; text-size-adjust:100%; text-align:left ">
                  A review of Gregory Clark’s claims in ‘A Farewell to Alms’
                </div>
                <br />
                <div class="text-subtitle2" style="font-family:'Rufina', serif">by Ishita Singh | September 15th, 2020</div>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <i style="font-size: 14px;font-family: 'Rufina', serif">The author critically analyses the existing literature to answer three developmental questions in Economic history posed by Gregory Clark – 1. The cause of Industrial Revolution, 2. Why it happened in England and 3. The cause for the Great Divergence. While accepting Clark’s argument of varying cultural traits as a possible cause for these events, the author also points out that culture in isolation cannot be the sole determinant for them. The author advances to explain how there are a myriad of other factors that need to be considered before reaching a definitive answer.
                </i>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    The world as we see around us today is amassed with inequality of disparate sorts. More often than not, we are susceptible to casually forgetting how or where it all even started while debating its presence, mostly because of its wide omnipresence. Inequality manifests itself in multiple facets of life: social, political, economic, etc. both within and across societies. Countries across the globe have worked tirelessly to diminish the persistence of this stubborn phenomena but have failed to entirely do so. Although, some countries, at least within their boundary, have managed to reduce its hold much more effectively than other countries. Ironically, by virtue of its very stubborn nature, just as these countries started to reduce inequality within their society, it paved its way into the gaps of inequality across nations, by further widening it. Such omnipresence and stubbornness of this phenomena irks the very fundamental question of how it even came into existence. Multiple economists over the decades have attempted to answer this very question through diverse theories and conflicting point of views. Gregory Clark, in his ambitious book ‘A Farewell to Alms’, published in 2007, delves into the realm of economic history to answer this question broadly from his own point of view, that has obscurely been touched by others.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    A <i>Farewell to Alms</i> seeks to provide a definitive answer to three fundamental questions of economic history: What caused the Industrial Revolution, why did it happen particularly in England, and why is there still a divergence, in terms of development, across countries? He does so by attempting to link the three questions on a chronological timeline by the thread of ‘culture’ or ‘cultural traits’. Clark argues that Malthusian pressures working upon a stable and static agrarian society, in England, lead to the birth of cultural traits that pushed the English economy towards Industrial Revolution. He claims that English people’s orientation and aspirations largely shifted towards ‘middle class’ attributes such as thrift, prudence, negotiation and hard work as opposed to previous attributes such as spendthrift, violence, impulsiveness and leisure. <sup>1</sup> He further argues that only England had the perfect ingredients (in the forms of cultural traits of the society) that were needed for the revolution to take place. Lastly, he explains how and why there is a Great Divergence across countries, even today, by attributing it to again cultural traits of the people living in these countries.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Clark manages to provide volumes of data and statistics to substantiate the arguments that he is making. In fact, he often taps into the most unconventional sources of data, like stature of men, wills of dying parents, etc. to make historical claims that support his argument. More often than not, he makes frequent simplistic claims using this data to further his argument. For example, while talking about the Malthusian working of the pre-industrial world, he uses birth and death rate data, along with the established relationship that ‘material living standard reduces with higher birth rates’ to draw the conclusion that a higher death rate will definitively improve living standards in a society. He even goes to the extent of saying that the reason behind Polynesia being a “paradise” is its high death rate due to infanticides, internal warfare and human sacrifice.<sup>2</sup>
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The arguments presented by Clark are new and interesting; however, not entirely indisputable. There is a wide array of existing literature that aims to offer answers to the same questions that Clark is attempting to answer, but with contending point of views. Jared Diamond in his book <i>‘Gun, Germs and Steel’</i> looks at the same questions but through the lens of geography, botany, zoology and natural resource endowments.<sup>3</sup> He tries to explain the inequality across countries by looking at what they have had in terms of natural resource endowment, geographical terrain, climatic environment, flora and fauna. Daron Acemoglu and James Robinson, in their book <i>‘Why Nations Fail’</i> on the other hand provide more widely appreciated answers to these question by highlighting the role of institutions in causing differential rates of development across countries.<sup>4</sup> By giving the case study of Nogales, a town divided into two parts between Mexico and USA by a border, and North Korea and South Korea, he builds up to his claim, that while both these cases had exactly the same geography, endowment, etc., there is a huge disparity in terms of development in both these cases owing solely to the nature of the institutions put into place.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    This is not to say that the argument provided by Clark is entirely flawed or faulty, but rather, that he may have been a little too hasty in dismissing all other contending point of views solely in pursuit of his supposedly all-pervasive argument. For instance, he openly claims that “… institutions as well shall see, often respond to economic circumstances rather than determine them”<sup>5</sup>. While his claim is partially true that institutions respond to economic circumstances, but to say that they do not determine or influence them at all is a little extreme to say the least. Institutions have in fact played a key role in shaping the economic circumstances and the economic environments worldwide. Also, the impact of institutions on culture is noteworthy as well. Culture is a fluid and dynamic phenomenon that constantly changes and alters with every small change in the society. A stark example of an institutional change impacting culture would be the introduction of reservations (an affirmative action policy) in India. It is one such institutional change that massively impacted the existing culture and demography of the Indian society: socially, politically and economically. This shows that it isn’t fair to turn a blind eye to the role of institutions in causing changes to the culture and circumstances of a country.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    One of the most ubiquitous contentions against Clark’s book is that it seems to be heavily fixated upon the concepts of ‘Social Darwinism, Natural selection and Survival of the Richest’. In his explanations for differences across nations, he talks about the disparity in the ‘quality of labour’ as a key reason behind why certain countries prospered during and post-industrial revolution, as opposed to others. This seems rather misguided and inadvertently directs a ‘colonial gaze’ at the problem. He argues that the only reason why, despite availability of similar technologies in both India and other successful countries, the mills in Bombay couldn’t gain a huge profit with their low-labour cost advantage was because the quality of workers was way worse there. There could be a myriad of other factors influencing this phenomenon, including but not restricted to institutions as well. In fact, this must be partially true too, but focusing solely on this while quickly dismissing or not paying heed to any other possible factor actually weakens his arguments and evidences as certain prejudices, although it may not be deliberate.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The book, without a doubt, has gained immense popularity and sold a huge number of copies, and helped in peaking people’s interest in the field of economic history, with its quirky facts and contentions. Yet, the book has simultaneously garnered many wide-reaching criticisms as well. While the data and statistics that Clarke provides is hard to dismiss, it isn’t quite as easy to accept them at face value either. As Benjamin M. Freidman, acknowledges in his review of this book, for the New York Times, it is particularly strange that Clarke chooses to opt for wills as his source of information on the number of children as opposed to the parish records of births and deaths.<sup>6</sup> On the bright side, the book successfully manages to wrestle with the subjective and dynamic field of ‘culture’ to explain wide economic phenomenon, which is increasingly rare to find in popular Economics literature, as it is mostly left for disciplines like Sociology or Anthropology. Clarke effectively manages to anchor culture as an important factor when considering the possible causation behind the industrial revolution and in fact, the Great Divergence. However, as it already is obvious, one cannot solely look at culture to explain the changes that took place throughout economic history. One needs to explore different point of views and be more accepting of them before presenting such a strong claim. It is essential to realize that in the real world, every phenomenon is characterized by multiple causations instead of a singular one. Unfortunately for us, the world doesn’t function on the assumption of ceteris paribus. Hence, while Clarke’s arguments are important and necessary, so are Diamond’s and Acemoglu and Robinson’s, and many others for that matter. One cannot expect one single factor to explain such a complex phenomenon that changed the course of humanity altogether. In econometrics terms, all these causes could be seen as independent (or even partially dependent) variables collectively affecting changes on the dependent variable of ‘growth’.
                </p>
