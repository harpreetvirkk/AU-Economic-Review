                <div class='title-url' style="font-size:35px; line-height:60px; font-family: 'Rufina', serif; text-align:left">
                  Slow Burn
                </div>
                <div class='title-url' style="font-size:1.5rem; line-height:1.4; font-weight:700; font-family: 'Rufina', serif; text-size-adjust:100%; text-align:left ">
                  The Impact of Covid-19 on MSMEs in India
                </div>
                <br />
                <div class="text-subtitle2" style="font-family:'Rufina', serif">by Kartikeya Bhatotia & Ridhi Aggarwal | September 8th, 2020</div>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <i style="font-size: 14px;font-family: 'Rufina', serif">The authors critically analyse the blow faced by Micro, Small & Medium Enterprises (MSME) sector in India due to lockdown imposed by the government as a precautionary meeasure for the Covid-19 pandemic. The following article decomposes the analysis through various verticals of Business Activity, Labour & Raw Material supply, Government Response and Liquidity position, providing a potential business outlook for the sector and a suitable way forward.
                </i>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    The Covid-19 pandemic has hit the economy severely ever since India went under lockdown in late March. Even though the economy is now on the path of recovery, the sudden economic slump has left especially the 6.99 <sup>1</sup> crore micro, small and medium enterprises (MSMEs) reeling under low demand, operations shutdown, inventory loss and bottlenecks.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    MSMEs, owing to their size, have less resilience and flexibility while facing economic shocks such as the one that the pandemic has created. This makes them particularly vulnerable to survive the prolonged crisis as compared to bigger counterparts. Moreover, MSMEs in India are overrepresented in sectors that are disproportionately affected by the pandemic, especially those that are characterised by demand shocks.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Immediate consequences of the pandemic and the subsequent lock-down range from shutting of a substantial portion of the MSMEs, fraying supply chains, decreased capacity to a sharp dip in demand. There is widespread evidence that the fallout of the pandemic would be long-term and goes beyond the present circumstances. MSMEs thus, stare at a volatile present and an uncertain future.
                </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
                    Business Activities and Capacity
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    When the state and nation-wide lockdowns began in March, most regions were at least a few weeks away from commencing the harvest season. Businesses that otherwise absorbed surplus labour in this lean agricultural period, particularly construction and brick-kilns <sup>2</sup>, totally ceased activity. Many eventually sprung back to activity by June, particularly in rural areas. However, some surveys suggest that more than a third of MSMEs<sup>3, 4</sup>  and self-employed individuals had shut their operations at least until normalcy returns. A large share of these MSMEs belongs to the manufacturing sector.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Though many MSMEs are slowly returning to increased capacities, only a quarter are producing at more than 50% capacity. Fortunately, the capacity graph is now up from less than 18% enterprises running at more than 50% capacity in June 2020.
                </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
                    Labour and raw material shortage
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    In the wake of a major migrant exodus from metros and industrial towns across India, concomitant with the suspension of much of public transport, many were worried about labour and raw material shortage. However, MSMEs are slowly getting back to their feet in this regard. According to a survey done by the National Small Industries Corporation, only 9% of MSMEs reported labour shortages, while 12% reported raw material shortages – some of which can be attributed to low demand and capital.
                </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
                    Government Response
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The government notably announced a Rs 1.76 lakh crore stimulus package to fight the crisis. One of the subsequent announcements included doubling of collateral-free loans to women SHGs from Rs 10 lakh to Rs 20 lakh. It was premised on the recommendations of the UK Sinha committee on MSMEs, which was submitted in 2019.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The key thrust was a huge push in credit guarantees to MSMEs of Rs. 3 lakh crore. The loans will have a tenure of 4 years and they will have a moratorium of 12 months (that is, the payback starts only after 12 months). The loan should be taken before October 31, 2020. In July, newer definitions of MSME were applied which will assess the category on turnover. Notably, there will be no virtual difference between a manufacturing MSME and a services MSME hereon.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    However, the government has come under fire for its own non-payments to the MSME sector. Additionally, the disbursement of the above-mentioned credit scheme’s distribution stood at just Rs 61,987 crore as on July 10 to 1.20 lakh beneficiaries<sup>5</sup>.
                </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
                    Liquidity Conundrum
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    At least 50% of MSMEs reported they were at a liquidity crunch <sup>6</sup> at the start of August 2020, over four months after the pandemic began. The government and the Reserve Bank of India have introduced several measures (mentioned above) for easing the liquidity problem, which was notably plaguing the MSME sector much before the pandemic.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    These measures are to infuse liquidity in the country’s economy through the banking system, with the aim of avoiding widespread job losses and a financial crisis<sup>7</sup>. However, despite all measures till now, the last-mile transmission of liquidity infused in the financial system remains a pipedream. About 85% MSMEs operate from households where the formal banking system still does not reach. Therefore, not only does the stimulus and credit package benefit large corporations, it is also coupled with the limited credit risk attitude of Indian banks while catering to MSME owners. Since MSMEs form a large part of the national GDP, the liquidity conundrum is bound to affect the entire economy.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    According to the initial results of a six-month survey carried out by LEAD at Krea University, 40% of MSMEs have responded to the liquidity crunch by attempting to borrow for expenses.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    However, only 14% of the borrowing was achieved from formal sources<sup>8</sup>. It is clear, therefore, that the liquidity crunch is here to stay.
                </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
                    Business Outlook:
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    A large majority (81%) of MSMEs remain confident of a recovery post-Covid19 according to the survey by LEAD at Krea University, even though as much as 57% of them reported they don’t have any cash reserves to survive. A survey done by Institute for Competitiveness in the immediate aftermath of the pandemic also reported general positivity by business owners wherein 90% of them affirmed that their business would return to normalacy within two years <sup>9</sup>. It was notably the medium scale enterprises who predicted larger recovery time.
                </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
                    Who have gained, and who have lost?
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The survey done by the National Small Industries Corporation identified sectors that are the most hard-hit and those which are doing relatively well during the crisis. The twin factors of health hazard and decreased disposable income have decimated the tourism sector. In addition to this, people are vary of large purchases, hitting the automobiles, consumer products and apparel sector. MSMEs tied to internet service, retailing and pharma have potentially benefitted from the pandemic. This can be attributed to especially those MSMEs who were able to offer e-commerce functionalities. According to a survey, for MSMEs in the retail and education services sector, revenue contribution from e-commerce stood at 53% and 65% respectively during the pandemic <sup>10</sup>.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The MSME ministry identifies that sectors like tourism, apparel, automotive manufacturers and gems and jewellery have been hit the worst, with an expected recovery of around 18 months. It also identified industrial belts in Gujarat, Haryana, Tamil Nadu, Rajasthan, West Bengal, Uttar in Pradesh, Karnataka and Maharashtra among the worst affected <sup>11</sup>.
                </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
                    Solutions and way forward
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Most MSME units will not be able to benefit from the government’s liquidity package, because they are either not connected to formal banking systems, or banks do not have the risk appetite to provide credit. Hence, Mudra loans can be arranged in these cases. Pradhan Mantri MUDRA Yojana (PMMY) scheme, launched on April 8, 2015, provides loans up to 10 lakh to the non-corporate, non-farm small/micro enterprises. These loans are classified as MUDRA loans under PMMY and are given by Commercial Banks, RRBs, Small Finance Banks, MFIs and NBFCs. Though a dip in demand is hard to tackle, evidence shows that MSMEs can benefit from e-commerce functionalities. The government should aid MSMEs to link their businesses online especially those whose supply chains are severely affected due to logistics. A separate fund for tech and communication upgrade is absolutely required at this stage to reduce costs and improve process efficiency.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The government notably was behind dues amounting to thousands of crores during the crisis – therefore better management and expedited processes are required for delayed payment monitoring portal of the government named ‘Samaadhaan’.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Since 99% of MSMEs are micro-enterprises with low growth potential and low employment outside the household, the pandemic threatens to shrink even the present levels of employment. Therefore, the government should introduce temporary wage subsidies for a few months to incentivize employers to retain their employees.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Finally, in order to solve the problem of low risk appetite of lending institutions in India, it is often recommended that the government should create a mechanism that closely tracks the fund utilisation and financial health of the borrowers and intervene at the first signs of distress<sup>13</sup>.
                </p>
