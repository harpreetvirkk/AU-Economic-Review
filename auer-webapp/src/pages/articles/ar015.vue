            <div style="padding: 10px 0px 10px 0px">
                <i style="font-size: 14px;font-family: 'Rufina', serif">The author explores how people's economic behaviour would change post a vaccine announcement for Covid-19. Analysing how uncertainty often leads to an increased incidence of fatalising risk taking behaviour, the author discusses how the behavioral impact of a vaccine announcement will differ across various income groups.
                </i>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    As Russia announces its Covid-19 vaccine named <i>Sputnik V</i>, registered as the world’s first coronavirus vaccine, several leading groups of scientists and pharma companies across the world can be expected to soon follow in the same direction. While such vaccine related announcements alter the perceptions of safety and are expected to affect economic behaviours of individuals, such behavioural changes might vary based on the economic profile of a person.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Despite the spike in Covid-19 cases in India after its almost four month lockdown, there is still a lack of widespread adherence to preventive measures such as social-distancing and wearing masks. The Nomura India Business Resumption Index (NIBRI) hints at a phase of economic recovery, owing to an increase in economic activity post-lockdown. At this juncture, the announcement of a vaccine for coronavirus could have a varied effect on people’s behaviour based on their income levels.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    There are now more than 45 lakh Covid cases in India, despite the mandatory nationwide lockdown that halted human movement and activity for nearly four months. In such cases, it is natural to think that preventive measures might not be sufficient and that one is bound to get infected. This fatalistic (fate-driven) thinking will increase the instances of risk-taking behaviour (Akesson et.al. 2020). A similar observation was made by Sandfort and Shidlo (2010) in a study conducted in New York where fatalistic thinking was linked to increased HIV risk behaviours.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    As a result of fatalistic thinking (augmented by lower death rate), we are seeing more people neglect recommended safety practices. There is also a rise in the economic activity as indicated by the recovery in demand for power (Mercom India), improvement in workplace mobility (Livemint) and fall in the unemployment rate (Business Standard) which is partly a result of such fatalistic and risk-taking behaviour, because people are now more willing to return to work and  go out to purchase goods and services, despite the fact that pandemic has not ended. One inference that can be drawn from this behaviour is that people will only continue following preventive measures if they truly believe it can save them from getting infected. If not, they would rather leave the occurrence of such events to fate, and indulge in more risk-taking behaviour. Therefore, the certainty of a vaccine will counter fatalistic behaviours, and vice versa. However, with Covid-19, a vaccine announcement might not be enough to counter fatalistic behaviours of people, especially from the low-income strata.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Before delving deeper into the Indian context, it is worth looking at available data on how spending recovery is happening among households from different income strata in the US and generalize it. As seen in Figure 1, from April to September the spending has rebounded especially among low-income households but spending of high income households is considerably lower and appears to be connected to perceived health risks (Opportunity Insights). One possible reason for such behaviours is how these groups weigh different risks.
                </p>
            </div>
