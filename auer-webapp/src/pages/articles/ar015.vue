            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    For a low-income household, which needs the economy running to survive, a risk-driven approach based on fatalistic thinking would seem less risky as compared to the high-income households. Since low-income households have less savings, earning money on a day-to-day basis is necessary requirement and they would align more towards taking risk sooner, driven by fatalistic thinking. This is evident as we see migrant labourers already returning to the cities for work. However, as the income level goes up, the fatalistic behaviour reduces. This happens because for a household with ample savings, it would seem riskier to venture out as they can manage household expenditure with their savings.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Based on this, we have a scale of income levels & risk-taking behaviours with low-income people who are prone to high level of fatalism induced risk-taking behaviours at one extreme and high-income individuals who are not prone to fatalism induced risk-taking behaviours at the other. As we move from low-income level to high-income level, the number of risks people take falls.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    When a vaccine is announced, the impact on economic behaviours of people varies depending on where they are placed on this spectrum. A vaccine brings with it the certainty that a disease can be prevented, countering the fate driven risky behaviour. However, we can’t expect much to change in individuals that are closer to the extremes. The high and low risk-takers feel they are better off being so because of the weights they have assigned to these risks. The others, who have started taking some risks like travelling, going to a shopping mall, attending a marriage, staying at a hotel, dining in a restaurant, going to a salon, etc. due to fatalistic beliefs, should start cutting back on their investments in these activities once a vaccine announcement is made.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    This happens because a certainty that they can overcome the threat of infection if they protect themselves for few more months makes investments in non-essential goods and services seem riskier. As per a survey by Bain & Company, consumer demand for food ordering-in, toys, beauty products, etc. has gone up in May. In the context of a vaccine being available, the risk of interacting with even one Covid patient will severely impact the sentiments of people and push them into being more cautious. Subsequently, we can expect to see shrinking markets, or a reduction in pace of economic recovery, till vaccination process is complete among the majority of the population.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    While a coronavirus vaccine will certainly better lives, livelihoods and the economy, we can still expect a shift back to risk-averse behaviour and reduced economic activity in the immediate period after the availability of a vaccine. The quantum of shift will depend on the percentage of people at different income levels. As the number of people who are able to access the vaccine increases over time, we can expect to see corresponding increase in market activity once again.
                </p>
