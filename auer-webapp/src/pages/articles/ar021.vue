                <div class='title-url' style="font-size:35px; line-height:60px; font-family: 'Rufina', serif; text-align:left">
                  Permanent Impairment of an Economic Muscle
                </div>
                <div class='title-url' style="font-size:1.5rem; line-height:1.4; font-weight:700; font-family: 'Rufina', serif; text-size-adjust:100%; text-align:left ">
                   Unequal Opportunities
                </div>
                <br/>
                <div class="text-subtitle2" style="font-family:'Rufina', serif">by Garima Bhalla & S Arunima | September 15th, 2020</div>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <i style="font-size: 14px;font-family: 'Rufina', serif">In this piece, the authors discuss the implications of COVID-19 on low-income segments of India, arguing that a turbulent future lies ahead with a further divergence between the have and the have-nots.
                </i>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    There is something peculiar about the Indian streets. It is illuminated by the energy of youth yet contaminated by poverty. According to the World Bank’s forecast, currently, India is a country of 1.3 billion people with a median age of 28.4. 60.8% of this population falls below the age of 35 and 58% of them are below the age of 19. Demographically 34.5% of the population is urban, leaving the majority of 65.5 % as rural. India had 364 million people living below the poverty line in 2015-16, the largest for any country. Did you get lost in these numbers? The core idea is that a large chunk of the young population is well within the ideal age bracket of pursuing education yet belongs to rural strata of India where scope of education is limited- thereby, making India a poor young nation.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    This is a cause of concern for India. Being home to a population of poor leads to its own set of problems which have further accelerated an already abysmal situation after COVID-19. One such issue is education among the poor and how the lack thereof leads to a permanent impairment of an economic muscle of the nation. Most of us are aware of the dire state of the government schools of our country. And if you are not, just try talking to your house help’s child or any child who is enrolled in a government school. If you happen to tutor them, even for a short while, you will often lose your cool not only because the child’s level of understanding and learning is below par but also because of the low level of motivation she has to pursue education. While government schools can majorly be held responsible for the former reason the latter reason holds even if the child is enrolled in a private school under a policy like that of Economically Weaker Sections (EWS).
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Liu and Chiang (2018) show in their research that a family’s socio-economic background plays a significant role in a student’s motivation to learn. Even though children from underprivileged families have capabilities, they are less encouraged to value learning and thus develop lower motivation to master their academic subjects as compared to their privileged peers. The pandemic has extended the concept of schools to the home and parents. Disadvantaged families fail to instill a high level of learning motivation in children due to low levels of literacy, social-emotional support and lack of resources. Katsiyannis (1991) also shows that summer breaks have a detrimental effect on students’ learning graph. Relating it to equity issues, Jamar (1994) shows that while the students from middle-class and upper-class families show improvement in reading and writing skills, students from lower-class families lose out on them. Students from families that speak languages other than English at home tend to lose on their English language skills as compared to their peers that come from English speaking families.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    While a summer break lasts for not more than 1.5-2 months in India and 3 months in any other country, COVID- 19 has led to a break of more than 5 months in the educational field. So, extrapolating the downfall in the student’s post-summer break learning graph for the break due to COVID-19 isn't a bizarre idea after all. Most of the private schools have shifted to the online mode of imparting education to children at their homes and government schools, due to the obvious lack of resources, cannot adopt the technological change. A 2018’s report by legislative rights group Indus Action, revealed that each year only 20% of the 20 Lakh of the reserved EWS seats in private schools gets filled. This implies that most underprivileged children rely on government schools for walking on the educational path. However, the point of concern here is, a private school or a government one, either which ways these students do not have the means to pursue online pedagogy. And thus, India welcomes a tremendous problem as discussed ahead.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The students from privileged families are once again ahead in the race of education and underprivileged ones are pushed further back. The problem becomes grave because the already existing lack of motivation coupled with the COVID-19 break will throw off the underprivileged children from the path of education. Consequently, they will cease to become a part of the skilled labour force. The extent of the damage caused to India will only be unveiled in the coming months. But what we know for a fact is, not all damages are going to be repairable and we have joined blocks to put forward one such damage. India once again has embodied the saying, “To him that hath, more shall be given; and from him, that hath not, the little that he hath shall be taken away.”
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    So far, we have established a machinery of propelling a never-ending income inequality which fuels and is fueled by education inequality. Is this worrisome for Indian Economy? Well, history shows a nation seldom witnesses a peaceful growth without catering to the issue of inequality. Raghuram Rajan in his book Fault Lines argues that inequality in the U.S. was the heart of the infamous 2008 crisis. Populist political parties cater to the issue of inequality by extending credit to the poor. Extending credit becomes a fruitful exercise when the poor are empowered and made capable of repaying the debt which almost is never focused on. Thus, financial institutions often come under the burden of lending to the underprivileged section of society against little or no security of repayment of loans.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Another dent in the economy due to inequality occurs due to the reduction in aggregate demand and an increase in unemployment rates. The popular economic reasoning behind this is that poor have relatively higher Marginal Propensity of Consumption. Moreover, inequality leads to lower productivity of the poor. Consequently, the economy is injected with anaemic wage growth and lower labour force participation. Furthermore, the observed trend of underinvestment in human capital by the poor devoid them of the high returns on investments such as education. On the flip side, inequality is equally bad for the rich and this should not be an alien idea. To have an essence of it, suppose that you are running a billion-dollar business, you need able customers to absorb the supply of your services and produce, the poor population definitely cannot take care of it.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The stated issue urges a need to incorporate a more fiscal oriented policy paradigm. The poor should be prioritized for vaccinations, once they are out. This would help restore normalcy in their life. A mentorship programme should be in place to instill motivation in the underprivileged children to resume education. Private sector should be engaged by roping in CSR to ensure every child is under the direct scrutiny of a mentor. The functionality and performance of the government schools should be monitored on a regular basis, starting from a check on students as well as teacher’s attendance and extending it to the students’ academic track record. New policies should be formulated, and the existing ones should be enforced, to extend the access of technology to the poor section of the society. This would aid in cushioning the extent of damages that any such future shocks would bring along. Emphasis should be put on vocational and skill-based training for the school dropouts. Private schools must spread awareness about the EWS policy and ensure a higher enrollment rate of vulnerable children at a disadvantage.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    The deep burns from the pandemic will not subside anytime soon in the future. India so far has largely focused on extending credit to the poor without making them capable of repaying their debts. Quoting a famous Chinese proverb here “Give a man a fish and you feed him for a day. Teach him how to fish and you feed him for a lifetime”. We have given them the fish rather than teaching them how to fish. If we allocate suitable budget towards implementing the measures stated above and make poor capable enough of standing on their own feet, their share of income and participation in the labour market might see an increase in the future. The pandemic should not cause a tear to this muscle that would hold back economic performance in the years to come. Instead, it should be an opportunity to restructure the economy and give India a stronger economic muscle.
                </p>
