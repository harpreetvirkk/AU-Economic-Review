                    This, along with value, availability, supply chain disturbances and convenience, strongly suggests that businesses and brands will witness sharp shifts in consumer loyalty. Only those that envision to cater to the needs of the consumer will see growth. Consequently, a fundamental change in customer acquisition is predominant. Firms need to work harder to connect with conscious consumers – with an overall long-term rise in the bargaining power of buyers. While enduring effects of the pandemic are yet to follow, it is safe to assume that that the post-Covid buyer will not simply be swayed by traditional market forces such as prices.
                </p>
