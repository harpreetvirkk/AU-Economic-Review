            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    Interestingly, the sharp change in spending patterns of consumers between each threshold is a testimony to how quickly consumers can process new information and subsequently make informed spending decisions.<sup>6</sup> The quarantine living preparation also saw an increase in digital commerce as consumers chose to buy online. Paradoxically, however, there also was an increase in support of local businesses.<sup>7</sup> Consumers desired local buying in show of support of community engagement as well as in need of knowing where their goods are coming from. In the spirit of safe and hygienic delivery of their goods, a majority of consumers are in fact planning on continuing with this behavior in the future.<sup>8</sup>
                </p>
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    Reversing this seed of panic and fear, both in spheres of health and financial anxiety, is an almost impossible task. Arguably then, the heightened emotions that the pandemic has accelerated suggests that these shifts in consumer spending patterns are in fact long-term. In particular, many argue that priority health buying, conscious consumption and the growing love for local, are trends that are here to stay. As this report by McKinsey & Company highlights, consumers are now predominantly looking to purchase from companies that have healthy and hygienic practices and demonstrate care and concern for their employees <sup>9</sup>. For instance, Domino’s Pizza spent over $11 million on pandemic related expenses for its workers such as sick pay and protective equipment <sup>10</sup> but still managed to make profits this quarter and continues its growth trajectory globally. In fact, it is on track to create 5000 new jobs across the UK.
                </p>
            </div>
