                </p>
                <b style="font-size: 18px; font-family: 'Rufina', serif">
                    Emerging Trends in Consumer Spending Patterns
                </b>
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    Economist Robert Shiller proposed the notion that COVID-19 has brought with it not one but two pandemics – the health pandemic and the pandemic of financial anxiety over the economic consequences of the first.<sup>1</sup> Attributing the effects of financial anxiety to the “affect heuristic” <sup>2</sup>, a term coined by psychologist Paul Slovic and colleagues at the University of Oregon, Shiller admits that at the onset of a tragic event, emotionally upset people tend to react out of fear even when the need for fear is absent. This heuristic can in fact be inferred as the reason for shifts in spending patterns in the early stages of the pandemic across multiple markets. It also explains the emergence of new habits in correlation to news cycles.
                </p>
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    Owing to the fear of the uncertain, a study by Nielsen <sup>3</sup> defines the first four shifts in consumer spending patterns during the early pandemic as a rise in <b> proactive health-minded buying, reactive health management, pantry preparation </b> and <b>quarantine living preparation</b>. The first two thresholds reflect the shift of consumer priorities towards essential virus containment and public safety products. Expectedly, sales of medical supplies in the US rose by 85% in February 2020 from a meagre 5% in December 2019.<sup>4</sup> The third and fourth thresholds manifest the panic mindset of the consumers as they stockpile necessities in anticipation of an uncertain and prolonged quarantine. For instance, the home entertainment industry saw an upward trend of consumer spending.<sup>5</sup>
                </p>
            </div>
            <div class="col-md-8" style="padding: 10px 40px 10px 40px">
